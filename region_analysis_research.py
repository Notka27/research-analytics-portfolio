    def _validate_adjacency_matrix(self, W):
        """Проверка матрицы смежности на соответствие требованиям CAR модели"""
        try:
            # 1. Проверка диагонали (должна быть нулевой)
            if not np.all(np.diag(W) == 0):
                raise ValueError("Диагональ матрицы W должна быть нулевой")

            # 2. Проверка неотрицательности
            if not np.all(W >= 0):
                raise ValueError("Матрица W должна содержать только неотрицательные значения")

            # 3. Проверка нормализации по строкам
            row_sums = W.sum(axis=1)
            if not np.allclose(row_sums, 1, atol=1e-8):
                self.logger.warning("Строки матрицы W не нормализованы. Нормализую...")
                W = W / row_sums[:, np.newaxis]

            # 4. Проверка связности графа
            n_components = connected_components(sparse.csr_matrix(W), directed=False, return_labels=False)
            if n_components > 1:
                # Автоматическое исправление несвязности графа
                self.logger.warning(f"Граф несвязный, содержит {n_components} компонент. Добавляю связи...")

                # Находим минимальное остовное дерево для связывания компонент
                mst = minimum_spanning_tree(sparse.csr_matrix(1/(1 + W)))
                W = W + mst.toarray()
                W = W / W.sum(axis=1)[:, np.newaxis]  # Повторная нормализация

            return W
        except Exception as e:
            self.logger.error(f"Ошибка валидации матрицы W: {str(e)}")
            raise

    def _prepare_car_matrix(self):
        """Улучшенное создание CAR матрицы"""
        try:
            centroids = self.geo_data.geometry.centroid
            coords = np.column_stack((centroids.x, centroids.y))

            # 1. Используем комбинацию KNN и радиального подхода
            k = min(5, len(coords)-1)
            nbrs = NearestNeighbors(n_neighbors=k+1).fit(coords)  # +1 чтобы исключить сам регион
            distances, indices = nbrs.kneighbors(coords)

            # 2. Создаем матрицу весов с гауссовским ядром
            sigma = np.median(distances[:, 1:])  # медианное расстояние до соседей
            W = np.zeros((len(coords), len(coords)))

            for i in range(len(coords)):
                for j in indices[i, 1:]:  # пропускаем первый элемент (сам регион)
                    d = np.linalg.norm(coords[i] - coords[j])
                    W[i, j] = np.exp(-(d**2)/(2*sigma**2))

            # 3. Делаем матрицу симметричной
            W = np.maximum(W, W.T)
            np.fill_diagonal(W, 0)

            # 4. Нормализуем по строкам
            row_sums = W.sum(axis=1)
            W = W / row_sums[:, np.newaxis]

            # 5. Проверяем и сохраняем матрицу
            W = self._validate_adjacency_matrix(W)

            # Визуализация
            plt.figure(figsize=(12, 10))
            plt.spy(W, precision=1e-8, markersize=5)
            plt.title("Улучшенная матрица смежности W")
            plt.savefig("W_matrix_improved.png", dpi=300)
            plt.close()

            return W
        except Exception as e:
            self.logger.error(f"Ошибка создания CAR матрицы: {str(e)}")
            raise

    def build_hierarchical_bayesian_model(self):
        """Улучшенная версия CAR модели с точками изменения и локальными трендами"""
        self.logger.info("\n=== ПОСТРОЕНИЕ МОДЕЛИ С ЛОКАЛЬНЫМИ ТРЕНДАМИ ===")

        try:
            if self.panel_data is None:
                self.prepare_panel_data()

            # Подготовка данных
            pca_cols = [f'PC{i+1}' for i in range(self.pca.n_components_)]
            X = self.panel_data[pca_cols + ['covid_period', 'post_covid', 'time', 'time_sq']].values
            y = self.panel_data[self.dependent_var].values

            regions = self.panel_data.index.get_level_values('Область').to_numpy()
            times = self.panel_data.index.get_level_values('year').astype(int).to_numpy()
            time_values = self.panel_data['time'].values
            time_sq_values = self.panel_data['time_sq'].values

            unique_regions = pd.factorize(regions)[0]
            unique_years = pd.factorize(times)[0]
            n_regions = len(np.unique(unique_regions))
            n_years = len(np.unique(unique_years))

            # Нормализация времени
            time_values_norm = (time_values - time_values.mean()) / time_values.std()

            # Подготовка улучшенной CAR матрицы
            W = self._prepare_car_matrix()
            W = np.asarray(W.todense() if sparse.issparse(W) else W)

            with pm.Model() as self.model:
                # 1. Основные коэффициенты с регуляризацией
                beta = pm.StudentT('beta', nu=4, mu=0, sigma=0.25, shape=X.shape[1])

                # 2. Параметры CAR модели с ограничениями для стабильности
                tau = pm.Gamma('tau', alpha=2, beta=0.5)
                alpha = pm.Uniform('alpha', lower=0, upper=0.99)

                # 3. Преобразуем матрицы в тензорные переменные
                W_tensor = pt.as_tensor_variable(W)
                D = pt.diag(pt.sum(W_tensor, axis=1))
                I = pt.eye(n_regions)

                # 4. Ковариационная матрица CAR процесса
                Q = tau * (D - alpha * W_tensor)

                # 5. Пространственные эффекты с использованием Cholesky разложения
                L = pm.Deterministic('L', pt.slinalg.cholesky(Q))
                spatial_effects_raw = pm.Normal('spatial_effects_raw', 0, 1, shape=n_regions)
                spatial_effects = pm.Deterministic('spatial_effects', pt.dot(L, spatial_effects_raw))

                # 6. Модель тренда с точками изменения
                beta_trend = pm.Normal('beta_trend', 0, 0.5)
                base_trend = pm.Deterministic('base_trend', beta_trend * time_values_norm)

                # Локальные тренды для каждого периода
                delta_pre = pm.Normal('delta_pre', 0, 0.1)
                delta_covid = pm.Normal('delta_covid', 0, 0.3)  # Большая изменчивость
                delta_post = pm.Normal('delta_post', 0, 0.2)

                trend = pm.Deterministic('trend',
                    base_trend +
                    delta_pre * (time_values < 5) * time_values_norm +  # 2015-2019 (time < 5)
                    delta_covid * ((time_values >= 5) & (time_values <= 7)) * (time_values_norm - 5) +  # 2020-2022
                    delta_post * (time_values > 7) * (time_values_norm - 7)  # 2023+
                )

                # 7. Годовые эффекты с принудительной идентификацией
                year_effects_raw = pm.Normal('year_effects_raw', 0, 1, shape=n_years)
                year_effects = pm.Deterministic('year_effects',
                    year_effects_raw - pt.mean(year_effects_raw))

                # 8. Моделирование дисперсии
                base_sigma = pm.HalfNormal('base_sigma', sigma=0.3)
                covid_multiplier = pm.Lognormal('covid_multiplier', mu=0, sigma=0.5)
                postcovid_multiplier = pm.Lognormal('postcovid_multiplier', mu=0, sigma=0.3)

                sigma = pm.Deterministic('sigma',
                    pt.switch(
                        self.panel_data['covid_period'].values == 1,
                        base_sigma * covid_multiplier,
                        pt.switch(
                            self.panel_data['post_covid'].values == 1,
                            base_sigma * postcovid_multiplier,
                            base_sigma
                        )
                    )
                )

                # 9. Линейный предиктор
                mu = pm.Deterministic('mu',
                    pm.math.dot(X[:, :-2], beta[:-2]) +
                    trend +
                    year_effects[unique_years] +
                    spatial_effects[unique_regions]
                )

                # 10. Правдоподобие с t-распределением
                nu = pm.Gamma('nu', alpha=2, beta=0.1)  # Степень свободы
                y_obs = pm.StudentT(
                    'y_obs',
                    nu=nu,
                    mu=mu,
                    sigma=sigma,
                    observed=y
                )

                # 11. Настройка семплера
                self.trace = pm.sample(
                    draws=6000,
                    tune=6000,
                    chains=8,
                    cores=8,
                    target_accept=0.99,
                    max_treedepth=15,
                    return_inferencedata=True,
                    random_seed=42,
                    compute_convergence_checks=True
                )

            # Проверка сходимости
            self._check_convergence()
            self._calculate_model_metrics()

        except Exception as e:
            self.logger.error(f"Ошибка построения модели: {str(e)}")
            raise RuntimeError(f"Не удалось построить модель: {str(e)}")

    def _check_convergence(self):
        """Проверка сходимости"""
        self.logger.info("\n=== ПРОВЕРКА СХОДИМОСТИ ===")

        try:
            var_names = [
                'beta', 'tau', 'alpha',
                'beta_trend', 'delta_pre', 'delta_covid', 'delta_post',
                'base_sigma', 'covid_multiplier', 'postcovid_multiplier',
                'nu'
            ]
            var_names = [v for v in var_names if v in self.trace.posterior]

            summary = az.summary(self.trace, var_names=var_names)

            max_rhat = summary['r_hat'].max()
            min_ess = summary['ess_bulk'].min()

            problem_params = summary[(summary['r_hat'] > 1.01) | (summary['ess_bulk'] < 400)]
            if not problem_params.empty:
                self.logger.warning(f"Проблемные параметры:\n{problem_params.index.tolist()}")

            n_divergences = int(self.trace.sample_stats.diverging.sum().item())

            self.logger.info(f"\nМаксимальный R-hat: {max_rhat:.3f} (должен быть < 1.01)")
            self.logger.info(f"Минимальный ESS: {min_ess:.1f} (должен быть > 400)")
            self.logger.info(f"Количество дивергенций: {n_divergences} (должно быть 0)")

            if max_rhat > 1.01 or min_ess < 400 or n_divergences > 0:
                self.logger.warning("ПРЕДУПРЕЖДЕНИЕ: Проблемы со сходимостью!")

                try:
                    problem_vars = problem_params.index.tolist()[:3]
                    if problem_vars:
                        az.plot_trace(self.trace, var_names=problem_vars, compact=True)
                        plt.savefig('problem_params_trace.png', dpi=150, bbox_inches='tight')
                        plt.close()
                        self.logger.info("Диагностические графики проблемных параметров сохранены в problem_params_trace.png")
                except Exception as e:
                    self.logger.warning(f"Не удалось построить trace plot: {str(e)}")

        except Exception as e:
            self.logger.error(f"Ошибка при проверке сходимости: {str(e)}")
            raise

    def _calculate_model_metrics(self):
        """Расчет метрик модели"""
        self.logger.info("\n=== ВЫЧИСЛЕНИЕ МЕТРИК МОДЕЛИ ===")

        try:
            y = self.panel_data[self.dependent_var].values
            n_observations = len(y)

            if 'mu' not in self.trace.posterior:
                raise ValueError("Отсутствует переменная 'mu' в trace")

            mu_samples = self.trace.posterior['mu'].values

            if mu_samples.ndim == 3:
                chains, draws, obs = mu_samples.shape
                if obs != n_observations:
                    raise ValueError(f"Несоответствие наблюдений: y={n_observations}, mu={obs}")
                self.bayesian_pred = mu_samples.mean(axis=(0,1))
            elif mu_samples.ndim == 4:
                chains, draws, _, obs = mu_samples.shape
                if obs != n_observations:
                    raise ValueError(f"Несоответствие наблюдений: y={n_observations}, mu={obs}")
                self.bayesian_pred = mu_samples.mean(axis=(0,1)).mean(axis=1)
            else:
                raise ValueError(f"Неожиданная размерность mu_samples: {mu_samples.shape}")

            r2 = r2_score(y, self.bayesian_pred)
            self.logger.info(f"\nR² модели: {r2:.3f}")

            n_samples = min(100, chains * draws)
            r2_samples = np.zeros(n_samples)

            for i in range(n_samples):
                chain_idx = i % chains
                draw_idx = (i // chains) % draws
                if mu_samples.ndim == 3:
                    sample = mu_samples[chain_idx, draw_idx]
                else:
                    sample = mu_samples[chain_idx, draw_idx].mean(axis=0)
                r2_samples[i] = 1 - ((y - sample)**2).sum() / ((y - y.mean())**2).sum()

            self.logger.info(
                f"Байесовский R²: {np.mean(r2_samples):.3f} "
                f"[95% HDI: {az.hdi(r2_samples)}]"
            )

            residuals = y - self.bayesian_pred
            shapiro_test = shapiro(residuals)
            self.logger.info(
                f"Тест Шапиро-Уилка: W={shapiro_test.statistic:.3f}, "
                f"p={shapiro_test.pvalue:.3f}"
            )

            self.last_r2 = r2

            # Расчет R² по регионам
            regions = self.panel_data.index.get_level_values('Область').unique()
            region_r2 = {}

            for region in regions:
                region_mask = self.panel_data.index.get_level_values('Область') == region
                y_region = y[region_mask]
                pred_region = self.bayesian_pred[region_mask]
                region_r2[region] = r2_score(y_region, pred_region)

            self.region_r2 = region_r2
            self.logger.info("\nR² по регионам:")
            for region, r2_val in region_r2.items():
                self.logger.info(f"{region}: {r2_val:.3f}")

            # Сохранение R² по регионам в файл
            r2_df = pd.DataFrame.from_dict(region_r2, orient='index', columns=['R2'])
            r2_df.index.name = 'Регион'
            r2_df.to_excel('region_r2_scores.xlsx')
            self.logger.info("Коэффициенты детерминации по регионам сохранены в region_r2_scores.xlsx")

        except Exception as e:
            self.logger.error(f"Ошибка при расчете метрик модели: {str(e)}")
            raise

    def _inverse_transform_predictions(self, forecast_samples):
        """Обратное преобразование прогнозов"""
        forecast_samples = np.asarray(forecast_samples)
        if forecast_samples.ndim == 1:
            forecast_samples = forecast_samples.reshape(-1, 1)

        def robust_quantile(x, q):
            return np.percentile(x, q * 100, method='linear')

        mean_pred = np.mean(forecast_samples, axis=0)
        lower_pred = robust_quantile(forecast_samples, 0.025)
        upper_pred = robust_quantile(forecast_samples, 0.975)

        forecast_mean = mean_pred * self.y_std + self.y_mean
        forecast_lower = lower_pred * self.y_std + self.y_mean
        forecast_upper = upper_pred * self.y_std + self.y_mean

        return forecast_mean, forecast_lower, forecast_upper

    def make_forecast(self):
        """Прогнозирование на 2024 год"""
        self.logger.info("\n=== ПРОГНОЗ НА 2024 ГОД ===")

        try:
            # Проверяем наличие данных за 2023 год
            if '2023' not in self.panel_data.index.get_level_values('year'):
                raise ValueError("Нет данных за 2023 год для построения прогноза")

            # Создаем копию данных за 2023 год для прогноза
            last_year_data = self.panel_data.xs('2023', level='year').copy()

            # Проверяем, что данные не пустые
            if last_year_data.empty:
                raise ValueError("Данные за 2023 год пусты")

            pca_cols = [f'PC{i+1}' for i in range(self.pca.n_components_)]

            # Подготавливаем данные для прогноза
            forecast_data = last_year_data.copy()

            try:
                # Устанавливаем прогнозируемый год
                forecast_data['year'] = '2024'
                forecast_data['covid_period'] = 0
                forecast_data['post_covid'] = 1
                forecast_data['time'] = 2024 - 2015  # Вычисляем time для 2024 года
                forecast_data['time_sq'] = forecast_data['time']**2

                # Проверяем наличие необходимых столбцов
                required_cols = pca_cols + ['covid_period', 'post_covid', 'time', 'time_sq']
                if not all(col in forecast_data.columns for col in required_cols):
                    missing = [col for col in required_cols if col not in forecast_data.columns]
                    raise ValueError(f"Отсутствуют необходимые столбцы: {missing}")

                X_forecast = forecast_data[required_cols].values
                regions = forecast_data.index.get_level_values('Область').to_numpy()

                # Получаем параметры из trace
                posterior = self.trace.posterior
                beta_samples = posterior['beta'].values
                spatial_effects_samples = posterior['spatial_effects'].values
                year_effects_samples = posterior['year_effects'].values

                # Параметры тренда
                beta_trend_samples = posterior['beta_trend'].values
                delta_pre_samples = posterior['delta_pre'].values
                delta_covid_samples = posterior['delta_covid'].values
                delta_post_samples = posterior['delta_post'].values

                # Дисперсия
                base_sigma_samples = posterior['base_sigma'].values
                postcovid_multiplier_samples = posterior['postcovid_multiplier'].values
                sigma_samples = base_sigma_samples * postcovid_multiplier_samples

                unique_regions = pd.factorize(regions)[0]
                forecast_samples = np.zeros((beta_samples.shape[0] * beta_samples.shape[1], len(regions)))

                # Вычисляем среднее и std времени для нормализации
                time_mean = self.panel_data['time'].mean()
                time_std = self.panel_data['time'].std()

                idx = 0
                for i in range(beta_samples.shape[0]):
                    for j in range(beta_samples.shape[1]):
                        for k, region in enumerate(regions):
                            try:
                                # Получаем время для региона
                                time_val = 2024 - 2015  # Фиксированное значение для 2024 года
                                time_norm = (time_val - time_mean) / time_std

                                # Базовый тренд
                                trend = beta_trend_samples[i,j] * time_norm

                                # Добавляем локальные тренды (для постковидного периода)
                                trend += delta_post_samples[i,j] * (time_norm - (2022-2015-time_mean)/time_std)

                                # Прогнозное значение
                                forecast_samples[idx, k] = (
                                    np.dot(X_forecast[k, :-2], beta_samples[i,j, :-2]) +
                                    trend +
                                    year_effects_samples[i,j, -1] +  # Последний годовой эффект
                                    spatial_effects_samples[i,j, unique_regions[k]] +
                                    np.random.normal(0, sigma_samples[i,j])
                                )
                            except Exception as e:
                                self.logger.warning(f"Ошибка прогноза для региона {region}: {str(e)}")
                                forecast_samples[idx, k] = np.nan
                        idx += 1

                # Обратное преобразование прогнозов
                forecast_mean, forecast_lower, forecast_upper = self._inverse_transform_predictions(forecast_samples)
                self._save_forecast_results(regions, forecast_mean, forecast_lower, forecast_upper)

            except Exception as e:
                self.logger.error(f"Ошибка подготовки данных прогноза: {str(e)}")
                raise

        except Exception as e:
            self.logger.error(f"Ошибка при прогнозировании: {str(e)}")
            raise RuntimeError(f"Ошибка прогнозирования: {str(e)}")

    def _save_forecast_results(self, regions, forecast_mean, forecast_lower, forecast_upper):
        """Сохранение результатов прогноза"""
        results = pd.DataFrame({
            'region': regions,
            'forecast_2024': forecast_mean,
            'lower_95': forecast_lower,
            'upper_95': forecast_upper
        })

        try:
            actual_2023 = self.panel_data.xs('2023', level='year')[self.dependent_var].values
            actual_2023 = actual_2023 * self.y_std + self.y_mean
            results['actual_2023'] = actual_2023
            results['change'] = results['forecast_2024'] - results['actual_2023']
            results['change_lower'] = results['lower_95'] - results['actual_2023']
            results['change_upper'] = results['upper_95'] - results['actual_2023']
        except Exception as e:
            self.logger.warning(f"Не удалось добавить фактические значения: {str(e)}")

        with pd.ExcelWriter('forecast_results_2024.xlsx') as writer:
            results.to_excel(writer, sheet_name='Прогноз', index=False)

            meta_info = pd.DataFrame({
                'Параметр': ['Дата прогноза', 'Количество регионов', 'R²', 'Среднее изменение'],
                'Значение': [pd.Timestamp.now().strftime('%Y-%m-%d'),
                            len(results),
                            getattr(self, 'last_r2', 'N/A'),
                            results['change'].mean()]
            })
            meta_info.to_excel(writer, sheet_name='Метаинформация', index=False)

        self.logger.info("Прогноз сохранен в forecast_results_2024.xlsx")

