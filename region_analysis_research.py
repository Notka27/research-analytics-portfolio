    def plot_sigma_comparison(self) -> None:
        """Сравнение дисперсий по периодам"""
        self.logger.info("\n=== СРАВНЕНИЕ ДИСПЕРСИЙ ПО ПЕРИОДАМ ===")

        try:
            fig, ax = plt.subplots(figsize=(10, 6))

            # Получаем sigma для каждого периода
            sigma_values = []
            periods = []

            # До ковида
            if 'base_sigma' in self.trace.posterior:
                sigma_values.extend(self.trace.posterior['base_sigma'].values.flatten())
                periods.extend(['До ковида'] * len(self.trace.posterior['base_sigma'].values.flatten()))

            # Ковид период
            if 'covid_multiplier' in self.trace.posterior and 'base_sigma' in self.trace.posterior:
                sigma_covid = self.trace.posterior['base_sigma'].values * self.trace.posterior['covid_multiplier'].values
                sigma_values.extend(sigma_covid.flatten())
                periods.extend(['Ковид'] * len(sigma_covid.flatten()))

            # После ковида
            if 'postcovid_multiplier' in self.trace.posterior and 'base_sigma' in self.trace.posterior:
                sigma_post = self.trace.posterior['base_sigma'].values * self.trace.posterior['postcovid_multiplier'].values
                sigma_values.extend(sigma_post.flatten())
                periods.extend(['После ковида'] * len(sigma_post.flatten()))

            df = pd.DataFrame({
                'Период': periods,
                'Дисперсия': sigma_values
            })

            sns.boxplot(x='Период', y='Дисперсия', data=df, ax=ax,
                       order=['До ковида', 'Ковид', 'После ковида'])
            ax.set_title("Сравнение дисперсий по периодам")
            ax.set_ylabel("Уровень дисперсии")
            ax.grid(True)

            plt.tight_layout()
            plt.savefig("sigma_comparison.png", dpi=300, bbox_inches="tight")
            plt.close()
            self.logger.info("График сравнения дисперсий сохранен в sigma_comparison.png")

        except Exception as e:
            self.logger.error(f"Ошибка при построении графика сравнения дисперсий: {str(e)}")
            raise

