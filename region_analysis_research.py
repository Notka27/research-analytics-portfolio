class PanelDataAnalyzer:
    def __init__(self, folder_path='/content', normalize=True):
        """
        Анализ данных с отдельными файлами для каждого показателя

        Parameters:
        -----------
        folder_path : str
            Путь к папке с файлами (по умолчанию '/content')
        normalize : bool
            Нужно ли нормализовать данные (по умолчанию True)
        """
        self.folder_path = folder_path
        self.normalize = normalize
        self.scalers = {}
        self.data = None
        self.y_col = 'y'  # Целевая переменная (веб-сайты)

        # Загрузка данных при инициализации
        self.load_data()

    def load_single_file(self, file_path):
        """Загрузка и обработка одного файла"""
        df = pd.read_excel(file_path)

        # Проверка обязательных колонок
        if 'Область' not in df.columns:
            raise ValueError(f"Файл {os.path.basename(file_path)} должен содержать колонку 'Область'")

        # Определение годов
        year_cols = [col for col in df.columns if str(col).isdigit()]
        if not year_cols:
            raise ValueError(f"В файле {os.path.basename(file_path)} не найдены колонки с годами")

        # Преобразование в длинный формат
        var_name = os.path.splitext(os.path.basename(file_path))[0]  # Имя файла без расширения
        long_df = df.melt(
            id_vars=['Область'],
            value_vars=year_cols,
            var_name='Год',
            value_name=var_name
        )
        long_df['Год'] = long_df['Год'].astype(int)

        return long_df

    def load_data(self):
        """Загрузка всех файлов с показателями"""
        print(f"\nЗагрузка данных из папки: {self.folder_path}")

        try:
            # Находим все xlsx-файлы в папке
            all_files = glob.glob(os.path.join(self.folder_path, '*.xlsx'))

            if not all_files:
                raise ValueError("Не найдено ни одного .xlsx файла в указанной папке")

            # Загружаем каждый файл и объединяем данные
            merged_data = None

            for file_path in all_files:
                file_name = os.path.basename(file_path)
                try:
                    df = self.load_single_file(file_path)

                    if merged_data is None:
                        merged_data = df
                    else:
                        merged_data = pd.merge(
                            merged_data,
                            df,
                            on=['Область', 'Год'],
                            how='outer'
                        )

                    print(f"Успешно загружен: {file_name}")

                except Exception as e:
                    print(f"Ошибка при загрузке {file_name}: {str(e)}")
                    continue

            if merged_data is None:
                raise ValueError("Не удалось загрузить ни один файл")

            self.data = merged_data

            # Проверяем наличие целевой переменной
            if self.y_col not in self.data.columns:
                raise ValueError(f"Не найдена целевая переменная '{self.y_col}' (файл y.xlsx)")

            # Получаем список всех предикторов
            self.predictors = [col for col in self.data.columns
                             if col.startswith('x') and col[1:].isdigit()]

            print(f"\nУспешно загружено:")
            print(f"- Регионов: {self.data['Область'].nunique()}")
            print(f"- Лет: {self.data['Год'].nunique()}")
            print(f"- Показателей: {len(self.predictors) + 1} (включая y)")

        except Exception as e:
            print(f"Ошибка при загрузке данных: {str(e)}")
            raise

    def prepare_data(self):
        """Подготовка данных для анализа"""
        if self.data is None:
            raise ValueError("Данные не загружены!")

        # Нормализация
        if self.normalize:
            print("\nНормализация данных...")
            for col in self.predictors + [self.y_col]:
                scaler = MinMaxScaler()
                self.data[col] = scaler.fit_transform(self.data[[col]])
                self.scalers[col] = scaler

        # Преобразование в panel-формат
        self.panel_data = self.data.set_index(['Область', 'Год']).sort_index()

        # Удаление строк с пропусками
        initial_size = len(self.panel_data)
        self.panel_data = self.panel_data.dropna()
        if len(self.panel_data) < initial_size:
            print(f"Удалено {initial_size - len(self.panel_data)} строк с пропусками")

        print("\nДанные подготовлены для анализа!")

    def within_correlations(self):
        """Within-корреляции (внутрирегиональные отклонения)"""
        if not hasattr(self, 'panel_data'):
            raise ValueError("Данные не подготовлены. Сначала вызовите prepare_data()")

        within_data = self.panel_data.groupby(level=0).transform(lambda x: x - x.mean())
        corr_matrix = within_data.corr()

        plt.figure(figsize=(16, 14))
        sns.heatmap(
            corr_matrix,
            annot=True,
            fmt=".2f",
            cmap='coolwarm',
            center=0,
            vmin=-1,
            vmax=1,
            square=True,
            linewidths=0.5
        )
        plt.title('Within-корреляции (внутрирегиональные отклонения)', pad=20)
        plt.xticks(rotation=45)
        plt.tight_layout()
        plt.show()

        # Анализ корреляций с y
        y_corr = corr_matrix[self.y_col].drop(self.y_col)
        print("\nWithin-корреляции с y (веб-сайты):")
        print(y_corr.sort_values(ascending=False).to_string(float_format="%.3f"))

        return corr_matrix

    def between_correlations(self):
        """Between-корреляции (межрегиональные средние)"""
        if not hasattr(self, 'panel_data'):
            raise ValueError("Данные не подготовлены. Сначала вызовите prepare_data()")

        between_data = self.panel_data.groupby(level=0).mean()
        corr_matrix = between_data.corr()

        plt.figure(figsize=(16, 14))
        sns.heatmap(
            corr_matrix,
            annot=True,
            fmt=".2f",
            cmap='coolwarm',
            center=0,
            vmin=-1,
            vmax=1,
            square=True,
            linewidths=0.5
        )
        plt.title('Between-корреляции (межрегиональные средние)', pad=20)
        plt.xticks(rotation=45)
        plt.tight_layout()
        plt.show()

        # Анализ корреляций с y
        y_corr = corr_matrix[self.y_col].drop(self.y_col)
        print("\nBetween-корреляции с y (веб-сайты):")
        print(y_corr.sort_values(ascending=False).to_string(float_format="%.3f"))

        return corr_matrix

    def nonlinear_dependency_analysis(self):
        """Анализ нелинейных зависимостей"""
        if not hasattr(self, 'panel_data'):
            raise ValueError("Данные не подготовлены. Сначала вызовите prepare_data()")

        if not hasattr(self, 'predictors') or not self.predictors:
            print("\nОшибка: Не найдены предикторы (x-переменные) для анализа")
            return None, None

        try:
            # Mutual Information
            mi = mutual_info_regression(
                self.panel_data[self.predictors],
                self.panel_data[self.y_col]
            )
            mi_series = pd.Series(mi, index=self.predictors, name='MI').sort_values(ascending=False)

            # RandomForest Feature Importance
            rf = RandomForestRegressor(n_estimators=100, random_state=42)
            rf.fit(self.panel_data[self.predictors], self.panel_data[self.y_col])
            rf_importance = pd.Series(rf.feature_importances_,
                                    index=self.predictors,
                                    name='RF_importance').sort_values(ascending=False)

            # Визуализация
            plt.figure(figsize=(14, 6))

            plt.subplot(1, 2, 1)
            mi_series.plot(kind='barh', color='teal')
            plt.title('Mutual Information (нелинейные связи)')
            plt.xlabel('Значение MI')

            plt.subplot(1, 2, 2)
            rf_importance.plot(kind='barh', color='darkorange')
            plt.title('Важность переменных (RandomForest)')
            plt.xlabel('Важность')

            plt.tight_layout()
            plt.show()

            print("\nТоп-5 нелинейных зависимостей (MI):")
            print(mi_series.head(5).to_string(float_format="%.4f"))
            print("\nТоп-5 важных переменных (RandomForest):")
            print(rf_importance.head(5).to_string(float_format="%.4f"))

            return mi_series, rf_importance

        except Exception as e:
            print(f"\nОшибка при анализе нелинейных зависимостей: {str(e)}")
            return None, None

    def temporal_correlation_analysis(self):
        """Анализ изменения корреляций по годам"""
        if not hasattr(self, 'panel_data'):
            raise ValueError("Данные не подготовлены. Сначала вызовите prepare_data()")

        yearly_corrs = {}
        for year in self.panel_data.index.get_level_values('Год').unique():
            year_data = self.panel_data.xs(year, level='Год')
            yearly_corrs[year] = year_data.corr()[self.y_col]

        yearly_corrs_df = pd.DataFrame(yearly_corrs).T.drop(self.y_col, axis=1)

        plt.figure(figsize=(14, 8))
        for col in yearly_corrs_df.columns:
            plt.plot(yearly_corrs_df.index, yearly_corrs_df[col], label=col, marker='o')

        plt.axhline(0, color='gray', linestyle='--')
        plt.title('Динамика корреляций с y (веб-сайты) по годам', pad=15)
        plt.xlabel('Год')
        plt.ylabel('Корреляция')
        plt.legend(bbox_to_anchor=(1.05, 1))
        plt.grid(alpha=0.3)
        plt.tight_layout()
        plt.show()

        # Анализ изменений
        changes = yearly_corrs_df.iloc[-1] - yearly_corrs_df.iloc[0]
        print("\nИзменение корреляций (последний год - первый год):")
        print(changes.sort_values(ascending=False).to_string(float_format="%.3f"))

        return yearly_corrs_df, changes

    def analyze(self):
        """Запуск полного анализа"""
        print("="*70)
        print(" ПОЛНЫЙ АНАЛИЗ РАЗВИТИЯ ИТ В РЕГИОНАХ РФ ".center(70))
        print("="*70)

        try:
            print("\n[1/5] Подготовка данных...")
            self.prepare_data()

            print("\n[2/5] Within-корреляции...")
            self.within_correlations()

            print("\n[3/5] Between-корреляции...")
            self.between_correlations()

            print("\n[4/5] Анализ нелинейных зависимостей...")
            self.nonlinear_dependency_analysis()

            print("\n[5/5] Временной анализ корреляций...")
            self.temporal_correlation_analysis()

        except Exception as e:
            print(f"\nОшибка при выполнении анализа: {str(e)}")
        finally:
            print("\n" + "="*70)
            print(" АНАЛИЗ ЗАВЕРШЕН ".center(70))
