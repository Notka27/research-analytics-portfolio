    def _run_heteroskedasticity_tests(self, resid, pred):
        """Тесты на гетероскедастичность"""
        print("\n1. Тест Бреуша-Пагана:")
        X_bp = np.column_stack([np.ones_like(pred), pred])
        bp_result = sm.OLS(resid**2, X_bp).fit()
        print(f" - LM статистика: {bp_result.fvalue:.3f}")
        print(f" - p-value: {bp_result.f_pvalue:.4f}")
        print("   H0: гомоскедастичность (p > 0.05)")

        print("\n2. Тест Уайта:")
        X_white = np.column_stack([pred, pred**2])
        white_result = sm.OLS(resid**2, sm.add_constant(X_white)).fit()
        white_stat = len(resid) * white_result.rsquared
        white_pvalue = chi2.sf(white_stat, white_result.df_model)
        print(f" - Статистика: {white_stat:.3f}")
        print(f" - p-value: {white_pvalue:.4f}")
        print("   H0: гомоскедастичность (p > 0.05)")

