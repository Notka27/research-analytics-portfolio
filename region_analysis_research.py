class RegionClusterAnalyzer:
    """
    Анализатор для кластеризации регионов с:
    - Автоматическим выбором нормализации и обработки выбросов
    - Настраиваемым количеством кластеров
    - Интерактивной визуализацией
    - Поддержкой нескольких методов кластеризации

    Параметры:
    ----------
    data_dir : str
        Директория с файлами данных
    file_prefix : str
        Префикс имен файлов
    file_range : range
        Диапазон номеров файлов
    n_clusters : int
        Количество кластеров
    min_year : int
        Минимальный год для анализа
    max_year : int
        Максимальный год для анализа
    scaling : ScalingMethod
        Метод масштабирования (можно строкой)
    outlier_method : OutlierMethod
        Метод обработки выбросов (можно строкой)
    exclude_files : List[str]
        Список файлов для исключения
    """

    def __init__(self, data_dir: str = "/content", file_prefix: str = "x",
                 file_range: range = range(1, 16), n_clusters: int = 4,
                 min_year: int = 2015, max_year: int = 2023,
                 scaling: Union[ScalingMethod, str] = ScalingMethod.AUTO,
                 outlier_method: Union[OutlierMethod, str] = OutlierMethod.AUTO,
                 exclude_files: List[str] = None):

        # Инициализация параметров
        self.data_dir = data_dir
        self.file_prefix = file_prefix
        self.file_range = file_range
        self.n_clusters = n_clusters
        self.min_year = min_year
        self.max_year = max_year
        self.scaling = scaling if isinstance(scaling, ScalingMethod) else ScalingMethod(scaling)
        self.outlier_method = outlier_method if isinstance(outlier_method, OutlierMethod) else OutlierMethod(outlier_method)
        self.exclude_files = exclude_files or ['x9', 'x11']

        # Данные и результаты
        self.raw_data = pd.DataFrame()
        self.features = pd.DataFrame()
        self.cluster_labels = np.array([])
        self.feature_importances = pd.DataFrame()
        self.permutation_importances = pd.DataFrame()
        self.linkage_matrix = None
        self.pca_result = None
        self.scaler = None
        self.outlier_handler = None

        # Состояния
        self._is_loaded = False
        self._is_preprocessed = False
        self._is_clustered = False

    def load_data(self) -> 'RegionClusterAnalyzer':
        """Загрузка и объединение данных из Excel-файлов"""
        logger.info("Загрузка данных...")
        try:
            all_data = []
            failed_files = []

            for i in self.file_range:
                file_name = f"{self.file_prefix}{i}"
                if file_name in self.exclude_files:
                    logger.info(f"Пропускаем исключенный файл: {file_name}")
                    continue

                file_path = os.path.join(self.data_dir, f"{file_name}.xlsx")
                try:
                    df = pd.read_excel(file_path)
                    if df.empty or len(df.columns) < 2:
                        logger.warning(f"Проблемы с файлом: {file_path}")
                        failed_files.append(file_path)
                        continue

                    # Стандартизация названий столбцов
                    df.columns = ['Область'] + [str(col) for col in df.columns[1:]]

                    # Преобразование и очистка данных
                    df.iloc[:, 1:] = df.iloc[:, 1:].apply(pd.to_numeric, errors='coerce')
                    df = df.fillna(df.median(numeric_only=True))

                    # Преобразование в длинный формат
                    melted = df.melt(
                        id_vars=['Область'],
                        var_name='Год',
                        value_name='Значение'
                    )
                    melted['Показатель'] = file_name
                    all_data.append(melted)

                except Exception as e:
                    logger.error(f"Ошибка при обработке {file_path}: {str(e)}")
                    failed_files.append(file_path)

            if not all_data:
                raise ValueError("Не удалось загрузить ни один файл")

            # Объединение всех данных
            self.raw_data = pd.concat(all_data, ignore_index=True)
            self.raw_data['Год'] = pd.to_numeric(self.raw_data['Год'], errors='coerce')

            # Фильтрация по годам
            self.raw_data = self.raw_data[
                (self.raw_data['Год'] >= self.min_year) &
                (self.raw_data['Год'] <= self.max_year)
            ].dropna(subset=['Год'])

            self._is_loaded = True
            logger.info(f"Успешно загружено {len(all_data)} файлов")
            if failed_files:
                logger.warning(f"Не удалось загрузить {len(failed_files)} файлов")

        except Exception as e:
            logger.error(f"Критическая ошибка при загрузке: {str(e)}")
            self._is_loaded = False

        return self

    def _handle_outliers(self, features: pd.DataFrame, method: Union[OutlierMethod, str] = None) -> pd.DataFrame:
        """Обработка выбросов выбранным методом"""
        if method is None:
            method = self.outlier_method

        if isinstance(method, str):
            method = OutlierMethod(method)

        logger.info(f"Обработка выбросов методом: {method.value}")

        try:
            if method == OutlierMethod.AUTO:
                # Автоматический выбор метода
                zscore_outliers = self._detect_outliers_zscore(features)
                iqr_outliers = self._detect_outliers_iqr(features)

                if zscore_outliers.sum().sum() < iqr_outliers.sum().sum():
                    return self._handle_outliers(features, OutlierMethod.ZSCORE)
                else:
                    return self._handle_outliers(features, OutlierMethod.IQR)

            elif method == OutlierMethod.IQR:
                return self._winsorize_iqr(features)

            elif method == OutlierMethod.ZSCORE:
                return self._winsorize_zscore(features)

            elif method == OutlierMethod.ISOLATION:
                return self._handle_outliers_isolation(features)

            else:
                logger.warning("Используется метод IQR по умолчанию")
                return self._winsorize_iqr(features)

        except Exception as e:
            logger.error(f"Ошибка обработки выбросов: {str(e)}")
            return features

    def _detect_outliers_iqr(self, features: pd.DataFrame) -> pd.DataFrame:
        """Обнаружение выбросов по межквартильному размаху"""
        Q1 = features.quantile(0.25)
        Q3 = features.quantile(0.75)
        IQR = Q3 - Q1
        return (features < (Q1 - 1.5 * IQR)) | (features > (Q3 + 1.5 * IQR))

    def _detect_outliers_zscore(self, features: pd.DataFrame, threshold: float = 3) -> pd.DataFrame:
        """Обнаружение выбросов по Z-скору"""
        z_scores = np.abs((features - features.mean()) / features.std())
        return z_scores > threshold

    def _winsorize_iqr(self, features: pd.DataFrame) -> pd.DataFrame:
        """Обработка выбросов методом winsorization на основе IQR"""
        Q1 = features.quantile(0.25)
        Q3 = features.quantile(0.75)
        IQR = Q3 - Q1

        lower_bound = Q1 - 1.5 * IQR
        upper_bound = Q3 + 1.5 * IQR

        features = features.copy()
        for col in features.columns:
            features[col] = features[col].clip(lower_bound[col], upper_bound[col])

        return features

    def _winsorize_zscore(self, features: pd.DataFrame, threshold: float = 3) -> pd.DataFrame:
        """Обработка выбросов методом winsorization на основе Z-скор"""
        mean = features.mean()
        std = features.std()

        lower_bound = mean - threshold * std
        upper_bound = mean + threshold * std

        features = features.copy()
        for col in features.columns:
            features[col] = features[col].clip(lower_bound[col], upper_bound[col])

        return features

    def _handle_outliers_isolation(self, features: pd.DataFrame) -> pd.DataFrame:
        """Обработка выбросов через изоляционный лес"""
        clf = IsolationForest(contamination='auto', random_state=42)
        outliers = clf.fit_predict(features)
        features = features.copy()
        features[outliers == -1] = features.median()
        return features

    def _auto_select_scaling(self, features: pd.DataFrame) -> ScalingMethod:
        """Автоматический выбор лучшего метода масштабирования"""
        methods = {
            ScalingMethod.STANDARD: StandardScaler(),
            ScalingMethod.MINMAX: MinMaxScaler(),
            ScalingMethod.ROBUST: RobustScaler(),
            ScalingMethod.POWER: PowerTransformer(method='yeo-johnson'),
            ScalingMethod.QUANTILE: QuantileTransformer(output_distribution='normal')
        }

        best_method = ScalingMethod.STANDARD
        best_score = -np.inf

        for method, scaler in methods.items():
            try:
                scaled_data = scaler.fit_transform(features)

                # Критерий 1: Нормальность распределения
                _, p_value = shapiro(scaled_data.flatten()[:5000])
                normality_score = p_value

                # Критерий 2: Стабильность модели
                model = Ridge()
                cv_scores = cross_val_score(model, scaled_data, np.random.rand(scaled_data.shape[0]),
                                         cv=3, scoring='r2')
                stability_score = np.mean(cv_scores)

                # Критерий 3: Сохранение структуры
                if len(features) > 1:
                    original_silhouette = silhouette_score(features, KMeans(n_clusters=3).fit_predict(features))
                    scaled_silhouette = silhouette_score(scaled_data, KMeans(n_clusters=3).fit_predict(scaled_data))
                    structure_score = 1 - abs(original_silhouette - scaled_silhouette)
                else:
                    structure_score = 0

                total_score = 0.4*normality_score + 0.3*stability_score + 0.3*structure_score

                if total_score > best_score:
                    best_score = total_score
                    best_method = method

            except Exception as e:
                logger.warning(f"Ошибка при оценке метода {method.value}: {str(e)}")
                continue

        logger.info(f"Автоматически выбран метод масштабирования: {best_method.value} (score: {best_score:.2f})")
        return best_method

    def preprocess_features(self, auto_scaling: bool = None, auto_outliers: bool = None) -> 'RegionClusterAnalyzer':
        """Подготовка и масштабирование признаков с обработкой выбросов"""
        if not self._is_loaded:
            logger.error("Данные не загружены")
            return self

        try:
            # Подготовка фичей
            features = self.raw_data.groupby(['Область', 'Показатель'])['Значение'].mean().unstack()
            features = features.fillna(features.median())

            # Обработка выбросов
            if auto_outliers or (auto_outliers is None and self.outlier_method == OutlierMethod.AUTO):
                features = self._handle_outliers(features)
            elif self.outlier_method != OutlierMethod.AUTO:
                features = self._handle_outliers(features, self.outlier_method)

            # Выбор масштабирования
            if auto_scaling or (auto_scaling is None and self.scaling == ScalingMethod.AUTO):
                selected_method = self._auto_select_scaling(features)
            else:
                selected_method = self.scaling

            # Применение масштабирования
            if selected_method == ScalingMethod.STANDARD:
                self.scaler = StandardScaler()
            elif selected_method == ScalingMethod.MINMAX:
                self.scaler = MinMaxScaler()
            elif selected_method == ScalingMethod.ROBUST:
                self.scaler = RobustScaler()
            elif selected_method == ScalingMethod.POWER:
                self.scaler = PowerTransformer(method='yeo-johnson')
            elif selected_method == ScalingMethod.QUANTILE:
                self.scaler = QuantileTransformer(output_distribution='normal')
            else:
                logger.warning("Используется StandardScaler по умолчанию")
                self.scaler = StandardScaler()

            self.features = pd.DataFrame(
                self.scaler.fit_transform(features),
                index=features.index,
                columns=features.columns
            )

            # PCA для визуализации
            self.pca_result = PCA(n_components=2).fit_transform(self.features)
            self._is_preprocessed = True
            logger.info("Признаки успешно подготовлены")

        except Exception as e:
            logger.error(f"Ошибка подготовки: {str(e)}")
            self._is_preprocessed = False

        return self

    def perform_clustering(self, method: Union[ClusterMethod, str] = 'hierarchical') -> 'RegionClusterAnalyzer':
        """Выполнение кластеризации выбранным методом"""
        if not self._is_preprocessed:
            logger.error("Признаки не подготовлены")
            return self

        if isinstance(method, str):
            method = ClusterMethod(method)

        try:
            logger.info(f"Выполнение кластеризации методом: {method.value}")

            if method == ClusterMethod.HIERARCHICAL:
                self.linkage_matrix = linkage(self.features, method='ward')
                model = AgglomerativeClustering(
                    n_clusters=self.n_clusters,
                    linkage='ward',
                    metric='euclidean'
                )
            elif method == ClusterMethod.KMEANS:
                model = KMeans(
                    n_clusters=self.n_clusters,
                    init='k-means++',
                    n_init=10,
                    random_state=42
                )
            elif method == ClusterMethod.DBSCAN:
                model = DBSCAN(eps=0.5, min_samples=5)
            else:
                logger.warning("Используется hierarchical по умолчанию")
                return self.perform_clustering(method=ClusterMethod.HIERARCHICAL)

            self.cluster_labels = model.fit_predict(self.features)
            self.raw_data['Cluster'] = self.raw_data['Область'].map(
                dict(zip(self.features.index, self.cluster_labels)))

            if len(np.unique(self.cluster_labels)) > 1:
                logger.info("\nОценка качества кластеризации:")
                logger.info(f"Силуэтный коэффициент: {silhouette_score(self.features, self.cluster_labels):.3f}")
                logger.info(f"Индекс Дэвиса-Болдина: {davies_bouldin_score(self.features, self.cluster_labels):.3f}")
                self._analyze_feature_importance()

            self._is_clustered = True
            logger.info(f"Успешная кластеризация ({method.value})")

        except Exception as e:
            logger.error(f"Ошибка кластеризации: {str(e)}")
            self._is_clustered = False

        return self

    def _analyze_feature_importance(self):
        """Анализ важности признаков для кластеров"""
        rf = RandomForestClassifier(n_estimators=100, random_state=42)
        rf.fit(self.features, self.cluster_labels)

        self.feature_importances = pd.DataFrame({
            'Feature': self.features.columns,
            'Importance': rf.feature_importances_
        }).sort_values('Importance', ascending=False)

        perm_result = permutation_importance(
            rf, self.features, self.cluster_labels,
            n_repeats=10, random_state=42, n_jobs=-1
        )

        self.permutation_importances = pd.DataFrame({
            'Feature': self.features.columns,
            'Permutation_Importance': perm_result.importances_mean,
            'Std': perm_result.importances_std
        }).sort_values('Permutation_Importance', ascending=False)

