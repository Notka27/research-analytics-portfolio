    def get_model_formula(self, precision: int = 4):
        """
        Возвращает математическую формулу модели с коэффициентами

        Параметры:
        - precision: количество знаков после запятой для коэффициентов
        """
        if self.model is None:
            raise ValueError("Модель не построена. Сначала выполните build_optimized_model().")

        coeffs = dict(zip(self.model.name_x, self.model.betas.flatten()))
        rho = self.model.betas[-2][0]

        formula_parts = []
        explanations = []

        if 'CONSTANT' in coeffs:
            const = coeffs['CONSTANT']
            formula_parts.append(f"{const:.{precision}f}")
            explanations.append("β₀ (константа)")

        for i in range(min(3, self.pca.n_components_)):  # Только первые 3 компоненты
            pc = f'PC{i+1}'
            if pc in coeffs:
                coef = coeffs[pc]
                formula_parts.append(f"{coef:.{precision}f}·{pc}")
                explanations.append(f"β_{i+1} (коэф. для {pc})")

        quadratic_terms = []
        if self.nonlinear_results is not None:
            nonlinear_pcs = self.nonlinear_results[
                (self.nonlinear_results['Quadratic_pvalue'] < 0.05) &
                (self.nonlinear_results['Nonlinear_Type'] != 'Linear')
            ].index.tolist()

            for pc in nonlinear_pcs:
                sq_term = f'{pc}_sq'
                if sq_term in coeffs:
                    coef = coeffs[sq_term]
                    quadratic_terms.append(f"{coef:.{precision}f}·{pc}²")
                    explanations.append(f"β_{pc}² (квадр. коэф. для {pc})")

        spatial_lag = f"{rho:.{precision}f}·W·y"
        formula_parts.append(spatial_lag)
        explanations.append("ρ (пространств. автокорреляция)")

        formula = f"{self.dependent_var} = " + " + ".join(formula_parts)

        if quadratic_terms:
            formula += " + " + " + ".join(quadratic_terms)

        formula += " + ε"

        explanation = "\n\nГде:\n" + "\n".join([f"  {item}" for item in explanations])
        explanation += "\n  W - матрица пространственных весов"
        explanation += "\n  ε - случайная ошибка"

        full_output = "МАТЕМАТИЧЕСКАЯ ФОРМУЛА МОДЕЛИ:\n\n" + formula + explanation

        if self.nonlinear_results is not None and len(nonlinear_pcs) > 0:
            full_output += "\n\nТОЧКИ ОПТИМУМА ДЛЯ НЕЛИНЕЙНЫХ КОМПОНЕНТ:\n"
            for pc in nonlinear_pcs:
                vertex = self.nonlinear_results.loc[pc, 'Optimal_Point']
                nl_type = self.nonlinear_results.loc[pc, 'Nonlinear_Type']
                full_output += (f"  {pc}: {vertex:.{precision}f} "
                               f"({nl_type.replace('U-shape', 'минимум').replace('Inverted U', 'максимум')})\n")

        return full_output

